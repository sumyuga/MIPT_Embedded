/*
A1 - Шифр Цезаря:
На стандартном потоке ввода задаётся целое неотрицательное число N и последовательность
допустимых символов в кодировке ASCII, оканчивающаяся точкой. Допустимые символы -
латинские буквы 'a' ... 'z', 'A' ... 'Z' и пробел. Требуется закодировать латинские буквы
('a' ... 'z', 'A' ... 'Z') шифром Цезаря, пробелы вывести без изменения. Число N задаёт сдвиг в шифре.
Шифр Цезаря заключается в следующем преобразовании. Пусть буквы алфавита пронумерованы от 0 до K - 1,
где K - число символов в алфавите. Тогда символ с номером n кодируется символом с номером
p = (n + N) mod K (mod - операция взятия остатка). На стандартном потоке вывода напечатать
зашифрованное сообщение, оканчивающееся точкой. Преобразование требуется выполнять независимо
для заглавных и строчных латинских букв. Указание: использовать массивы запрещается.
Example:
Input: 0 Hello World.
Output: Hello World.
*/

#include <stdio.h>

int main(void)
{
    int n;
    int c;

    scanf("%d", &n);
    n %= 26;

    c = getchar();
    if (c == ' ' || c == '\n' || c == '\r' || c == '\t') {
        c = getchar();
    }

    while (c != '.') {
        if (c >= 'a' && c <= 'z') {
            c = 'a' + (c - 'a' + n) % 26;
        } else if (c >= 'A' && c <= 'Z') {
            c = 'A' + (c - 'A' + n) % 26;
        }

        putchar(c);
        c = getchar();
    }

    putchar('.');
    putchar('\n');
    return 0;
}
